#include<iostream>
#include<fstream>
#include<string>
using namespace std;
void outFile()
{
	ofstream ofs;
	ofs.open("hello.txt");
		ofs << "Hello World!" << endl;
	ofs.close();
}
void readFile()
{
	ifstream ifs;
	ifs.open("hello.txt");
	if (ifs.fail())
	{
		cout << "读取失败";
		return;
	}
	string str;
	getline(ifs, str);
	cout << str << endl;
}
int main()
{
	outFile();
	readFile();
	return 0;
}